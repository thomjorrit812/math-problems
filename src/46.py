def is_even(num):
    """
    Checks if a number is even.
    
    Parameters:
    num (int): The number to check.
    
    Returns:
    bool: True if num is even, False otherwise.
    """
    return num % 2 == 0

# Example usage:
print(is_even(4))  # Output: True
print(is_even(5))  # Output: False
