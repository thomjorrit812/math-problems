def calculate_sum(a, b):
    """
    Calculate the sum of two numbers.
    
    Parameters:
    a (int/float): The first number.
    b (int/float): The second number.
    
    Returns:
    int/float: The sum of a and b.
    """
    return a + b

# Example usage
result = calculate_sum(5, 3)
print(result)  # Output: 8
